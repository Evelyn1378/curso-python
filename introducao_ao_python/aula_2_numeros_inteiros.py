# Aula 2: Números inteiros

# Type: int

example_1 = 15
example_2 = 100
example_3 = 5
example_4 = 3

# Operaçoes matemáticas
# Adição, subtração, multiplicação, divisão, exponenciação, divisão inteira, módulo

# Adição
addition_result = example_2 + example_3
print("Esse é o resultado da adição: ", addition_result)

# Subtração
subtraction_result = example_2 - example_3 - example_1
print("Esse é o resultado da subtração: ", subtraction_result)

# Multiplicação
multiplication_result = example_1 * example_3
print("Esse é o resultado da multiplicação: ", multiplication_result)

# Divisão
division_result = example_2 / example_3
print("Esse é o resultado da divisão: ", division_result)

# Exponenciação
exponentiation_result = example_4 ** example_3
print("Esse é o resultado da exponenciação: ", exponentiation_result)

# Divisão inteira
integer_division_result = example_2 // example_4
print("Esse é o resultado da divisão inteira: ", integer_division_result)

# Módulo
modulo_result = example_2 % example_4
print("Esse é o resultado do módulo: ", modulo_result)
